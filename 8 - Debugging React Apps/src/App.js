import React, { useState } from "react";

import CourseGoalList from "./components/CourseGoals/CourseGoalList/CourseGoalList";
import CourseInput from "./components/CourseGoals/CourseInput/CourseInput";
import "./App.css";

const App = () => {
  const [courseGoals, setCourseGoals] = useState([
    { text: "Do all exercises!", id: "g1" },
    { text: "Finish the course!", id: "g2" },
  ]);

  const addGoalHandler = (enteredText) => {
    setCourseGoals((prevGoals) => {
      const updatedGoals = [...prevGoals];
      /*
        Web Console

        Warning: Encountered two children with the same key, `goal1`. 
        Keys should be unique so that components maintain their identity across updates. 
        Non-unique keys may cause children to be duplicated and/or omitted â€” the behavior is unsupported and could change in a future version.
      */
      //updatedGoals.unshift({ text: enteredText, id: "goal1" });
      updatedGoals.unshift({ text: enteredText, id: Math.random().toString() });
      return updatedGoals;
    });
  };

  const deleteItemHandler = (goalId) => {
    setCourseGoals((prevGoals) => {
      const updatedGoals = prevGoals.filter((goal) => goal.id !== goalId);
      return updatedGoals;
    });
  };

  let content = (
    <p style={{ textAlign: "center" }}>No goals found. Maybe add one?</p>
  );

  if (courseGoals.length > 0) {
    content = (
      <CourseGoalList items={courseGoals} onDeleteItem={deleteItemHandler} />
    );
  }

  return (
    /* 
    >Terminal

    ERROR! :  Adjacent JSX elements must be wrapped in an enclosing tag.
      <section id="goal-form">
        <CourseInput onAddGoal={addGoalHandler} />
      </section>
      <section id="goals">
        {content}
      </section>
    */

    <div>
      <section id="goal-form">
        {
          // >Terminal
          // ERROR! : 'addGoalsHandler' is not defined
          // <CourseInput onAddGoal={addGoalsHandler} />
        }
        <CourseInput onAddGoal={addGoalHandler} />
      </section>
      <section id="goals">{content}</section>
    </div>
  );
};

export default App;
